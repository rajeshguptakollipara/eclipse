package flight;

public class flight {
	
	public int addnum(int a, int b)
	{
		return (a + b);
	}

	public int subnum(int a, int b)
	{
		return (a - b);
	}
	public int mulnum(int a, int b)
	{
		return (a * b);
	}
	public int divnum(int a, int b)
	{
		return (a / b);
	}
	
	
}
